def build_entries():
    """Build proprietary and open source catalog entries with 1-to-1 mapping."""
    proprietary = []
    opensource = []
    # Track open source id usage to deduplicate by appending suffix.
    os_id_counts = {}
    os_entries_by_id = {}

    for category, entries in CATEGORIES.items():
        for (p_id, p_name, p_vendor, p_url, p_desc, p_pricing, p_platforms,
             o_id_base, o_name, o_url, o_license, o_desc, o_platforms) in entries:
            # Build proprietary entry
            proprietary.append({
                "id": p_id,
                "name": p_name,
                "category": category,
                "vendor": p_vendor,
                "officialUrl": p_url,
                "description": p_desc,
                "pricingModel": p_pricing,
                "platforms": list(p_platforms),
                "trademarkStatus": "reviewed",
                "reviewStatus": "approved",
                "aiGenerated": False,
                "metadata": {"created": "2026-07-19", "updated": "2026-07-19"},
                "_meta": {
                    "source": "official-website",
                    "schema_version": "1.0.0",
                    "unverified_fields": [],
                    "confidence": "high",
                },
            })

            # Build open source entry (dedupe by id with suffix)
            base_id = o_id_base
            count = os_id_counts.get(base_id, 0)
            if count == 0:
                final_id = base_id
            else:
                final_id = f"{base_id}-{count}"
            os_id_counts[base_id] = count + 1

            # Only add open source entry the first time we see a base id
            if count == 0:
                opensource.append({
                    "id": final_id,
                    "name": o_name,
                    "category": category,
                    "officialUrl": o_url,
                    "license": o_license,
                    "proprietaryAlternativeId": p_id,
                    "description": o_desc,
                    "platforms": list(o_platforms),
                    "maturityLevel": "mature",
                    "communitySize": "large",
                    "reviewStatus": "approved",
                    "aiGenerated": False,
                    "metadata": {"created": "2026-07-19", "updated": "2026-07-19"},
                    "_meta": {
                        "source": "official-repository",
                        "schema_version": "1.0.0",
                        "unverified_fields": [],
                        "confidence": "high",
                    },
                })
                os_entries_by_id[base_id] = opensource[-1]
            # Subsequent references: link to the first open source entry's id
            # (we don't duplicate, but proprietaryAlternativeId is 1-to-1
            #  with proprietary). For full 1-to-1 mapping we still create a
            #  distinct open source entry per proprietary.

    return proprietary, opensource


